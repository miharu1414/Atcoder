vector<ll> make_divisors(ll n){
    vector<ll> lower_divisors, upper_divisors;
    ll i = 1;
    while(i * i <= n){
        if(n % i == 0){
            lower_divisors.push_back(i);
            if(i != n / i){
                upper_divisors.push_back(n / i);
            }
        }
        i++;
    }
    for(ll j = upper_divisors.size() - 1; j >= 0; j--){
        lower_divisors.push_back(upper_divisors[j]);
    }
    return lower_divisors;
}

